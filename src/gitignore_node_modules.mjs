import { file_read } from './file_read.mjs';
import { file_empty_if_not_exists } from './file_empty_if_not_exists.mjs';
import { string_split_new_line } from './string_split_new_line.mjs';
import { file_write } from './file_write.mjs';
export async function gitignore_node_modules() {
    let file_path = '.gitignore';
    await file_empty_if_not_exists(file_path);
    let existing = await file_read(file_path);
    let lines = string_split_new_line(existing);
    let node_modules = 'node_modules';
    list_add_if_not_exists(node_modules);
    let transformed = string_join_new_line(lines);
    await file_write(file_path, transformed);
}


function list_add_if_not_exists(node_modules) {
    if (!list.includes(node_modules)) {
        list_add(node_modules);
    }
}

