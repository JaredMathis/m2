import { string_split_new_line } from './string_split_new_line.mjs';
import { list_add_if_not_exists } from './list_add_if_not_exists.mjs';
import { string_join_new_line } from './string_join_new_line.mjs';
import { file_transform } from './file_transform.mjs';
export async function gitignore_node_modules() {
    await file_transform('.gitignore', transform);
    function transform(existing) {
        let lines = string_split_new_line(existing);
        list_add_if_not_exists(lines, 'node_modules');
        let transformed = string_join_new_line(lines);
        return transformed;
    }

}


